/// @page NamingParametersPage Naming Parameters
/// Beyond the obvious reason of readability, it is important to follow the guidelines for parameter names because parameters are displayed in documentation and in the designer when visual design tools provide Intellisense and class browsing functionality.
///
/// <b>√ DO</b> use camelCasing in parameter names.
///
/// <b>√ DO</b> use descriptive parameter names.
///
/// <b>√ CONSIDER</b> using names based on a parameter’s meaning rather than the parameter’s type.
///
/// @section NamingOperatorOverloadParametersSection Naming Operator Overload Parameters
/// <b>√ DO</b> use left and right for binary operator overload parameter names if there is no meaning to the parameters.
///
/// <b>√ DO</b> use value for unary operator overload parameter names if there is no meaning to the parameters.
///
/// <b>√ CONSIDER</b> meaningful names for operator overload parameters if doing so adds significant value.
///
/// <b>X DO NOT</b> use abbreviations or numeric indices for operator overload parameter names.
///
