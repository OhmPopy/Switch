/// @page NamingResourcesPage Naming Resources
/// Because localizable resources can be referenced via certain objects as if they were properties, the naming guidelines for resources are similar to property guidelines.
///
/// <b>√ DO</b> use PascalCasing in resource keys.
///
/// <b>√ DO</b> provide descriptive rather than short identifiers.
///
/// <b>X DO NOT</b> use language-specific keywords of the C++ languages.
///
/// <b>√ DO</b> use only alphanumeric characters and underscores in naming resources.
///
/// <b>√ DO</b> use the following naming convention for exception message resources.
///
/// The resource identifier should be the exception type name plus a short identifier of the exception:
///
/// <b>ArgumentExceptionIllegalCharacters</b>
///
/// <b>ArgumentExceptionInvalidName</b>
///
/// <b>ArgumentExceptionFileNameIsMalformed</b>
///
